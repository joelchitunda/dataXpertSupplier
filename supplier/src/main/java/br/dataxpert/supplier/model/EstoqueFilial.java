package br.dataxpert.supplier.model;

public class EstoqueFilial {

	private String filial;
	private String codproduto;
	private String descproduto;
	private String estoque;
	private String preco;
	private String ean;
	private String classevenda;
	private String dias;
	private String qtpedida;

	
	public String getFilial() {
		
		return filial;
		
	}

	public void setFilial(String filial) {
		
		this.filial = filial;
		
	}

	public String getCodproduto() {
		
		return codproduto;
		
	}

	public void setCodproduto(String codproduto) {
		
		this.codproduto = codproduto;
		
	}

	public String getDescproduto() {
		
		return descproduto;
		
	}

	public void setDescproduto(String descproduto) {
		
		this.descproduto = descproduto;
		
	}

	public String getEstoque() {
		
		return estoque;
		
	}

	public void setEstoque(String estoque) {
		
		this.estoque = estoque;
		
	}

	public String getPreco() {
		
		return preco;
		
	}

	public void setPreco(String preco) {
		
		this.preco = preco;
		
	}

	public String getEan() {
		
		return ean;
		
	}

	public void setEan(String ean) {
		
		this.ean = ean;
		
	}

	public String getClassevenda() {
		
		return classevenda;
		
	}

	public void setClassevenda(String classevenda) {
		
		this.classevenda = classevenda;
		
	}

	public String getDias() {
		
		return dias;
		
	}

	public void setDias(String dias) {
		
		this.dias = dias;
		
	}

	public String getQtpedida() {
		
		return qtpedida;
		
	}

	public void setQtpedida(String qtpedida) {
		
		this.qtpedida = qtpedida;
		
	}

}