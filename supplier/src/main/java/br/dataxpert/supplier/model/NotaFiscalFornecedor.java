package br.dataxpert.supplier.model;

public class NotaFiscalFornecedor {
	
	private String numeronota;
	private String dataemissao;
	private String valornota;
	private String peso;
	private String filial;
	private String uf;
	private String chavenfe;
	private String cnpjfilial;
	private String prazo;

	
	public String getNumeronota() {
		
		return numeronota;
		
	}

	public void setNumeronota(String numeronota) {
		
		this.numeronota = numeronota;
		
	}

	public String getDataemissao() {
		
		return dataemissao;
		
	}

	public void setDataemissao(String dataemissao) {
		
		this.dataemissao = dataemissao;
		
	}

	public String getValornota() {
		
		return valornota;
		
	}

	public void setValornota(String valornota) {
		
		this.valornota = valornota;
		
	}

	public String getPeso() {
		
		return peso;
		
	}

	public void setPeso(String peso) {
		
		this.peso = peso;
		
	}

	public String getFilial() {
	
		return filial;
		
	}

	public void setFilial(String filial) {
		
		this.filial = filial;
		
	}

	public String getUf() {
		
		return uf;
		
	}

	public void setUf(String uf) {
		
		this.uf = uf;
		
	}

	public String getChavenfe() {
		
		return chavenfe;
		
	}

	public void setChavenfe(String chavenfe) {
		
		this.chavenfe = chavenfe;
		
	}

	public String getCnpjfilial() {
		
		return cnpjfilial;
		
	}

	public void setCnpjfilial(String cnpjfilial) {
		
		this.cnpjfilial = cnpjfilial;
		
	}

	public String getPrazo() {
		
		return prazo;
		
	}

	public void setPrazo(String prazo) {
		
		this.prazo = prazo;
		
	}

}
