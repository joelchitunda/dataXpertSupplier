package br.dataxpert.supplier.model;

public class ItemPedidoFilial {
	
	private String numeropedido;
	private String filial;
	private String codproduto;
	private String descproduto;
	private String unidade;
	private String qtdepedida;
	private String qtdeentregue;
	private String valorunitario;
	private String multiplo;
	private String obs;

	
	public String getNumeropedido() {
		
		return numeropedido;
		
	}

	public void setNumeropedido(String numeropedido) {
		
		this.numeropedido = numeropedido;
		
	}

	public String getFilial() {
		
		return filial;
		
	}

	public void setFilial(String filial) {
		
		this.filial = filial;
		
	}

	public String getCodproduto() {
		
		return codproduto;
		
	}

	public void setCodproduto(String codproduto) {
		
		this.codproduto = codproduto;
		
	}

	public String getDescproduto() {
		
		return descproduto;
		
	}

	public void setDescproduto(String descproduto) {
		
		this.descproduto = descproduto;
		
	}

	public String getUnidade() {
		
		return unidade;
		
	}

	public void setUnidade(String unidade) {
		
		this.unidade = unidade;
		
	}

	public String getQtdepedida() {
		
		return qtdepedida;
		
	}

	public void setQtdepedida(String qtdepedida) {
		
		this.qtdepedida = qtdepedida;
		
	}

	public String getQtdeentregue() {
		
		return qtdeentregue;
		
	}

	public void setQtdeentregue(String qtdeentregue) {
		
		this.qtdeentregue = qtdeentregue;
		
	}

	public String getValorunitario() {
		
		return valorunitario;
		
	}

	public void setValorunitario(String valorunitario) {
		
		this.valorunitario = valorunitario;
		
	}

	public String getMultiplo() {
		
		return multiplo;
		
	}

	public void setMultiplo(String multiplo) {
		
		this.multiplo = multiplo;
		
	}

	public String getObs() {
		
		return obs;
		
	}

	public void setObs(String obs) {
		
		this.obs = obs;
		
	}

}