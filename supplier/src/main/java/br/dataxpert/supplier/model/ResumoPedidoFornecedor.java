package br.dataxpert.supplier.model;

public class ResumoPedidoFornecedor {
	
	private String ano;
	private String mes;
	private String qtde;
	private String valorpedido;
	private String valorfaturado;
	

	public String getAno() {
		
		return ano;
		
	}

	public void setAno(String ano) {
		
		this.ano = ano;
		
	}

	public String getMes() {
		
		return mes;
		
	}

	public void setMes(String mes) {
		
		this.mes = mes;
		
	}

	public String getQtde() {
	
		return qtde;
		
	}

	public void setQtde(String qtde) {
		
		this.qtde = qtde;
		
	}

	public String getValorpedido() {
		
		return valorpedido;
		
	}

	public void setValorpedido(String valorpedido) {
		
		this.valorpedido = valorpedido;
		
	}

	public String getValorfaturado() {
		
		return valorfaturado;
		
	}

	public void setValorfaturado(String valorfaturado) {
		
		this.valorfaturado = valorfaturado;
		
	}

}